#include <iostream>
using namespace std;
int main(){
	cin.tie(0)->sync_with_stdio(0);
	long long n, i; cin >> n;
	for(i = 0x100000000; i >= 0; i >>= 1){
		if (n & i) break;
	}
	cout << (n != i ? (i - 1) : ((i >> 1) - 1)) + (n + 1) / 2;
}

